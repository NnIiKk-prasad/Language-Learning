// -----Data Types in C-----
#include <stdio.h>

int main()
{
    int integer1 = 7;            // 2 to 4 bytes
    short integer4 = 8;          // 2 bytes
    unsigned short integer2 = 8; // 2 bytes
    long integer3 = 8;           // 4 bytes

    float b = 8.0;              // 4 bytes, 6 decimal places precision
    double c = 7.45;            // 8 bytes, 15 decimal places prcision
    long double d = 7.43567532; // 12  bytes, 19 decimal places precision

    char e = 't'; // 1 byte
    return 0;
}
