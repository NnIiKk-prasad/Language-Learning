// -----Assignment Operators in C-----
#include <stdio.h>

void main()
{
    int a = 5;  // Assignment
    printf("%d\n", a);

    a += 3;  // Compound assignment
    printf("%d\n", a);
    
    a -= 3;  // Compound assignment
    printf("%d\n", a);
    
    a *= 3;  // Compound assignment
    printf("%d\n", a);
    
    a /= 3;  // Compound assignment
    printf("%d\n", a);
    
    a %= 3;  // Compound assignment
    printf("%d\n", a);

}