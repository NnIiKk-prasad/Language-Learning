#  -----If, Else & Elif Conditionals-----
var1 = 56
var2 = int(input("Enter a number:"))
if var2 > var1:
    print("Entered number is greater than 56")
elif var2 == var1:
    print("Entered number is equal to 56")
else:
    print("Entered number is less than 56")

list1 = [5, 7, 3]
if var2 in list1:
    print("Entered number is in the list")
else:
    print("Entered number is not in the list")
