# -----String Slicing-----

my_str = "Nikhil is a good boy"
print(my_str[4])
print(my_str[0:6])   # include 0 but exclude 6
print(my_str[0:45])
print(my_str[0:])
print(my_str[:6])
print(my_str[0:6:2])
print(my_str[0::3])
print(my_str[0::])
print(my_str[17:19])
print(my_str[-3:-1])
print(my_str[::-1])    # to reverse string
print(my_str[::-2])
