# -----Operators-----
# Arithmetic operators
print("5 + 6 =", 5 + 6)
print("5 - 6 =", 5 - 6)
print("5 * 6 =", 5 * 6)
print("5 / 6 =", 5 / 6)
print("5 % 3 =", 5 % 3)
print("5 ** 3 =", 5 ** 3)
print("15 // 6 =", 15 // 6)
print()

# Assignment operators
x = 5
x += 7
print(x)
x %= 7
print(x)
print()

# Comparison operators
i = 5
print(i == 8)
print(i != 8)
print(i <= 8)
print(i >= 8)
print()

# Logical operators
a = True
b = False
print(a and b)
print(a or b)
print()

# Identity operators
print(a is b)
print(a is not b)
print()

# Membership operators
list1 = [2, 3, 55, 64, 100]
print(55 in list1)
print(64 not in list1)
print()

# Bitwise operators
print(0 & 1)
print(0 | 3)
